#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	i;

	i = 0;
	if (!s)
		s = "(null)";
	while (*s)
	{
		ft_putchar(*s);
		s++;
		i++;
	}
	return (i);
}
